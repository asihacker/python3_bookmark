import sys

print(sys.path)  # path是一个目录列表，供Python从中查找第三方扩展模块
print(sys.platform)  # 返回操作系统平台名称
print(sys.version)  # 获取Python解释器的版本信息
print(sys.getdefaultencoding())  # 返回当前默认的字符编码格式
print(sys.argv)  # cmd 命令参数列表
print(sys.exit(0))  # 退出程序，0表示正常退出 1表示异常退出
